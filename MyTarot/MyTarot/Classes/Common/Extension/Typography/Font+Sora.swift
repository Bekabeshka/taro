import SwiftUI

extension Font {
    static func soraBold(size: CGFloat) -> Font {
        .custom("Sora-Bold", size: size)
    }
    
    static func soraExtraBold(size: CGFloat) -> Font {
        .custom("Sora-ExtraBold", size: size)
    }
    
    static func soraExtraLight(size: CGFloat) -> Font {
        .custom("Sora-ExtraLight", size: size)
    }
    
    static func soraLight(size: CGFloat) -> Font {
        .custom("Sora-Light", size: size)
    }
    
    static func soraMedium(size: CGFloat) -> Font {
        .custom("Sora-Medium", size: size)
    }
    
    static func soraRegular(size: CGFloat) -> Font {
        .custom("Sora-Regular", size: size)
    }
    
    static func soraSemiBold(size: CGFloat) -> Font {
        .custom("Sora-SemiBold", size: size)
    }
    
    static func soraThin(size: CGFloat) -> Font {
        .custom("Sora-Thin", size: size)
    }
}
